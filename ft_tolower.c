int ft_tolower(int c)
{
    if (c > 64 && c < 91)
    {
        c = c + 32;
        return (c);
    }
    return (0);
}

#include <stdio.h>
int main()
{
    printf("%c\n", ft_tolower('B'));
    return (0);
}